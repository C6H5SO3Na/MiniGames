	//-------------------------------------------------------------------
	//「初期化」タスク生成時に１回だけ行う処理
	bool  Object::Initialize()
	{
		//スーパークラス初期化
		__super::Initialize(defGroupName, defName, true);
		//リソースクラス生成orリソース共有
		this->res = Resource::Create();

		//★データ初期化
		gameCnt = 0;

		//BGM
		bgm::LoadFile("bgmCG", "./data/sound/bgm/tanoshiimugibatake.mp3");
		bgm::Play("bgmCG");
		//★タスクの生成
		auto cgpm = ClassifyGamePM::Object::Create(true); //プレイヤマネージャーを生成
		//auto bguim = ClassifyGUIM::Object::Create(true);
		return  true;
	}
	//-------------------------------------------------------------------
	//「終了」タスク消滅時に１回だけ行う処理
	bool  Object::Finalize()
	{
		bgm::Stop("bgmCG");
		//★データ＆タスク解放
		ge->KillAll_G("ClassifyGamePM");
		if (!ge->QuitFlag() && this->nextTaskCreate) {
			//★引き継ぎタスクの生成
			
			Game::Object::CreateTask(4);
		}

		return  true;
	}
	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		gameCnt++;
		if (gameCnt == 1319) {
			Kill();
		}
	}
